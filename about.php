        <span>Про клуб:<br><br></span>
        <article>«Дина́мо» — український футбольний клуб з міста Києва, що бере участь в українській Прем'єр-лізі.
        «Динамо» засновано 1927 року, це найстарший на сьогодні професійний клуб міста. Єдиний клуб, який провів усі сезони у найвищій лізі СРСР, а потім України.
        Найтитулованіша команда України, має в активі 15 чемпіонських титулів, 11 національних Кубків, 8 Суперкубків України, 1 Суперкубок УЄФА і два Кубки володарів кубків.
        Крім того, за радянський період історії «Динамо» завоювало 13 чемпіонських титулів, 9 Кубків СРСР, 3 Суперкубки СРСР, що робить «Динамо» найтитулованішою командою в історії радянського футболу.
        Перша немосковська команда, що здобула титул чемпіона СРСР, та перший представник Радянського Союзу, який завоював європейський трофей.
        Найуспішніший клуб України за результатами семи десятиліть: 1930-х, 1940-х, 1950-х, 1960-х, 1970-х, 1980-х і 1990-х. Найтитулованіший клуб Центральної та Східної Європи.
        <br><br>
        Також «Динамо» заняло 8 місце в списку найкращих клубів світу 20-го століття за версією журналу Kicker, 16 місце в списку найкращих європейських клубів 20-го століття за версією IFFHS та 17 місце в списку найлегендарніших клубів світу за версією журналу France Football.
        Найкращий клуб Східної Європи 20-го століття за версією France-Presse.
        За даними аналітичного агентства CK, за підсумками 2011 року «Динамо» було першим в Україні та 19-м у Європі футбольним клубом за кількістю реальних прихильників.
